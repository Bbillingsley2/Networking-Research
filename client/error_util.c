// compare two binary strings of len l
// put the bit differences in xorbuf and return hamming distance

int hdcmp(unsigned char *b1, unsigned char *b2, unsigned char *xorbuf,int l)
{
    int i;
    int hd = 0;

    bzero(xorbuf,l);

    for(i=0; i < l; i++)
    {
	xorbuf[i] = b1[i] ^ b2[i];
	hd += hd_table[xorbuf[i]];
    }
    return(hd);
}
